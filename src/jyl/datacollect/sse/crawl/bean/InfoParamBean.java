public class InfoParamBean {
	String value ;
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	String itemName;
	String []blockBegin;
	String []blockEnd;
	String comment;//中文名
	public InfoParamBean( String itemName, String []blockBegin,
			String []blockEnd, String comment) {
		super();
		this.itemName = itemName;
		this.blockBegin = blockBegin;
		this.blockEnd = blockEnd;
		this.comment = comment;
	}
	public InfoParamBean() {
		super();
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public String getItemName() {
		return itemName;
	}
	public String[] getBlockBegin() {
		return blockBegin;
	}
	public String []getBlockEnd() {
		return blockEnd;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public void setBlockBegin(String[] blockBegin) {
		this.blockBegin = blockBegin;
	}
	public void setBlockEnd(String[] blockEnd) {
		this.blockEnd = blockEnd;
	}
}
